#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *p1;

	p1 = s1;
	while (*p1)
		p1++;
	while (*s2)
	{
		*p1++ = *s2++;
	}
	*p1 = '\0';
	return (s1);
}
